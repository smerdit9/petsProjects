function shuffle(arr) {
  for (let i = 0; i < arr.length; ++i) {
    let j = Math.floor(Math.random() * (i + 1));
    let temp = arr[i];
    arr[i] = arr[j];
    arr[j] = temp;
  };
  return arr;
}

